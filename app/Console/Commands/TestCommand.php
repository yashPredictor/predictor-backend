    private function fetchPlayerInfoAndStats(string $search, int $paged = 1, int $perPage = 10): ?array
    {
        $url = sprintf('%s/players/%s/advancestats', $this->advancedBaseUrl, $search);

        $response = Http::withHeaders([
            'x-auth-user' => $this->apiKey,
        ])
            ->get($url);

        if ($response->failed()) {
            Log::warning('Advanced stats unavailable', [
                'pid' => $search,
                'status' => $response->status(),
            ]);
            return null;
            throw new \RuntimeException(sprintf(
                'HTTP %s: %s',
                $response->status(),
                $response->body()
            ));
        }

        $payload = $response->json();

        if ($payload === null) {
            $decoded = json_decode($response->body(), true);
            $payload = is_array($decoded) ? $decoded : null;
        }
        $array = [];
        $array['stats'] = $payload['response'] ?? null;
        return $array;
    }

    /**
     * @param array<int, string> $players
     * @return array<string, string>
     */
    private function buildPlayerNameIndex(array $players): array
    {
        $index = [];

        foreach ($players as $playerId => $playerName) {
            $normalized = $this->normalizePlayerName((string) $playerName);
            if ($normalized === '') {
                continue;
            }

            $index[$normalized] = (string) $playerId;
        }

        return $index;
    }

    private function normalizePlayerName(string $name): string
    {
        $normalized = preg_replace('/\s+/', ' ', trim($name));

        if ($normalized === null) {
            return '';
        }

        return strtolower($normalized);
    }


    private function storeTeamPlayers(string $teamId, array $players): void
    {
        $this->firestore
            ->collection('teamPlayers')
            ->document($teamId)
            ->set([
                'teamId' => $teamId,
                'response' => $players,
                'lastFetched' => new Timestamp(now()->toDateTimeImmutable()),
            ]);
    }

    /**
     * @return array<int, string>
     */
    private function getStoredPlayerNames(): array
    {
        $documents = $this->firestore->collection('teamPlayers')->documents();
        $flat = [];

        foreach ($documents as $snapshot) {
            if (!$snapshot->exists()) {
                continue;
            }

            $data = $snapshot->data();
            $names = $this->extractPlayerNames($data['response'] ?? []);

            if (empty($names)) {
                continue;
            }

            foreach ($names as $name) {
                $flat[$name] = $name;
            }
        }

        return array_values($flat);
    }

    /**
     * @param mixed $payload
     * @return array<int, string>
     */
    private function extractPlayerNames($payload): array
    {
        $names = [];
        $queue = [$payload];

        while ($queue) {
            $current = array_pop($queue);

            if (!is_array($current)) {
                continue;
            }

            if (isset($current['name'], $current['id'])) {
                $names[$current['id']] = (string) $current['name'];
                continue;
            }

            foreach ($current as $value) {
                if (is_array($value)) {
                    $queue[] = $value;
                }
            }
        }

        return array_values($names);
    }

    /**
     * @return array<int, string>
     */
    private function getAdvancedStatsPids(): array
    {
        $documents = $this->firestore->collection('playerAdvancedStats')->documents();
        $pids = [];

        foreach ($documents as $snapshot) {
            if (!$snapshot->exists()) {
                continue;
            }

            $this->accumulatePids($snapshot->data(), $pids);
        }

        return array_values($pids);
    }

    private function outputArray(string $label, array $values): void
    {
        $this->line($label . ':');
        $this->line(var_export($values, true));
    }

    /**
     * @param mixed $payload
     * @param array<string, string> $pids
     */
    private function accumulatePids($payload, array &$pids): void
    {
        if (!is_array($payload)) {
            return;
        }

        if (isset($payload['pid'])) {
            $pid = $payload['pid'];
            if ($pid !== '') {
                $pids[$pid] = $pid;
            }
        }

        foreach ($payload as $value) {
            if (is_array($value)) {
                $this->accumulatePids($value, $pids);
            }
        }
    }
