    private function initializeClients(): FirestoreClient
    {
        $keyPath = $this->firestoreSettings['sa_json'] ?? config('services.firestore.sa_json');
        $projectId = $this->firestoreSettings['project_id'] ?? config('services.firestore.project_id');

        if (!$projectId && $keyPath && is_file($keyPath)) {
            $json = json_decode(file_get_contents($keyPath), true);
            $projectId = $json['project_id'] ?? null;
        }

        if (!$projectId) {
            throw new \RuntimeException(
                'Firestore project id missing. Set FIRESTORE_PROJECT_ID or provide a service account JSON with project_id.'
            );
        }

        $options = ['projectId' => $projectId];
        if ($keyPath && is_file($keyPath)) {
            $options['keyFilePath'] = $keyPath;
        }

        $this->apiKey = $this->cricbuzzSettings['key'] ?? config('services.cricbuzz.key');
        if (!$this->apiKey) {
            throw new \RuntimeException('Cricbuzz API key is not configured.');
        }

        return new FirestoreClient($options);
    }

    private function syncTeamPlayers(): void
    {
        $documents = $this->firestore->collection('teamsList')->documents();

        $synced = 0;
        $skipped = 0;
        $failed = 0;
        $seenTeamIds = [];

        foreach ($documents as $snapshot) {
            if (!$snapshot->exists()) {
                $skipped++;
                continue;
            }

            $teams = $this->extractTeams((array) $snapshot->data());

            if (empty($teams)) {
                $this->warn(sprintf('No teams found in document "%s".', $snapshot->id()));
                $skipped++;
                continue;
            }

            foreach ($teams as $teamData) {
                $teamId = isset($teamData['teamId']) ? (string) $teamData['teamId'] : null;

                if (!$teamId) {
                    $skipped++;
                    continue;
                }

                if (isset($seenTeamIds[$teamId])) {
                    $skipped++;
                    continue;
                }

                $seenTeamIds[$teamId] = true;

                try {
                    $apiResponse = $this->fetchTeamPlayers($teamId);
                } catch (Throwable $e) {
                    $failed++;
                    $this->error(sprintf('Failed to fetch players for team %s: %s', $teamId, $e->getMessage()));
                    Log::error('test_command.fetch_team_players_failed', [
                        'team_id' => $teamId,
                        'exception' => $e,
                    ]);
                    continue;
                }

                if ($apiResponse === null) {
                    $failed++;
                    $this->warn(sprintf('Skipping team %s due to empty API response.', $teamId));
                    continue;
                }

                try {
                    $this->storeTeamPlayers($teamId, $apiResponse);
                    $synced++;
                } catch (Throwable $e) {
                    $failed++;
                    $this->error(sprintf('Failed to save players for team %s: %s', $teamId, $e->getMessage()));
                    Log::error('test_command.store_team_players_failed', [
                        'team_id' => $teamId,
                        'exception' => $e,
                    ]);
                }
            }
        }

        $this->info(sprintf(
            'Team players sync finished. Synced: %d, skipped: %d, failed: %d',
            $synced,
            $skipped,
            $failed
        ));
    }

    /**
     * @param array<mixed> $payload
     * @return array<int, array<string, mixed>>
     */
    private function extractTeams(array $payload): array
    {
        $teams = [];
        $queue = [$payload];

        while (!empty($queue)) {
            $current = array_pop($queue);

            if (!is_array($current)) {
                continue;
            }

            if (isset($current['teamId'])) {
                $teams[] = $current;
                continue;
            }

            foreach ($current as $value) {
                if (is_array($value)) {
                    $queue[] = $value;
                }
            }
        }

        return $teams;
    }

    /**
     * @return array<int, mixed>|array<string, mixed>|null
     */
    private function fetchTeamPlayers(string $teamId): ?array
    {
        $url = sprintf('%s/teams/v1/%s/players', $this->baseUrl, $teamId);

        $response = Http::withHeaders([
            'x-auth-user' => $this->apiKey,
            'no-cache' => 'true',
        ])->get($url);

        if ($response->failed()) {
            throw new \RuntimeException(sprintf(
                'HTTP %s: %s',
                $response->status(),
                $response->body()
            ));
        }

        $payload = $response->json();
        if ($payload === null) {
            $decoded = json_decode($response->body(), true);
            $payload = is_array($decoded) ? $decoded : null;
        }

        return $payload;
    }

